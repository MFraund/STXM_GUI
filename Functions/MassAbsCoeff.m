function ucompound_out = MassAbsCoeff(compound, energy)




%% C pre-edge 278 eV
uc_278=1932;
un_278=3729;
uo_278=5972;
uh_278=454;
us_278=47343;
una_278=17682;
uk_278=5689;
ucl_278=50183;
ufe_278=16125;
uca_278=6902;
uzn_278=24523;
upb_278=10471;
ual_278=31584;
usi_278=37413;




%% C post-edge 320 eV
uc_320=39508;
un_320=2527;
uo_320=4187;
uh_320=288;
us_320=36405;
una_320=12447;
uk_320=47728;
ucl_320=39398;
ufe_320=12287;
uca_320=5437;
uzn_320=19315;
upb_320=12670;
ual_320=22363;
usi_320=28313;



%% Ca pre-edge (1st energy) 347 eV
uc_347=33165;
un_347=1959;
uo_347=3437;
uh_347=222.5;
us_347=31396;
una_347=10240;
uca_347=44637;
uk_347=41860;
ucl_347=34380;
ufe_347=10446;
uzn_347=16570;
upb_347=13473;
ual_347=18074;
usi_347=23740;

%% Ca post-edge (2nd energy) 352 eV

uc_352=32128;
un_352=1871;
uo_352=3316;
uh_352=212.3;
us_352=30563;
una_352=9871;
uca_352=43474;
uk_352=41022;
ucl_352=33540;
ufe_352=10097;
uzn_352=16145;
upb_352=13603;
ual_352=17434;
usi_352=23042;



%%_______________________________________________________
%%_______________Calculate Organic Cross Sections________

uad_278 = (6*12*uc_278 + 12*1*uh_278 + 4*16*uo_278)/...
    (6*12+12*1+4*16);%% adipic acid C6H12O4
uad_320 = (6*12*uc_320+12*1*uh_320+4*16*uo_320)/...
    (6*12+12*1+4*16);

ugluc_278=(6*12*uc_278+12*1*uh_278+6*16*uo_278)/...
    (6*12+12*1+6*16);%% glucose C6H12O6
ugluc_320=(6*12*uc_320+12*1*uh_320+6*16*uo_320)/...
    (6*12+12*1+6*16);%% glucose C6H12O6

uoxal_278=(2*12*uc_278+2*1*uh_278+4*16*uo_278)/...
    (2*12+2*1+4*16);%% oxalic acid C2H2O4
uoxal_320=(2*12*uc_320+2*1*uh_320+4*16*uo_320)/...
    (2*12+2*1+4*16);%% oxalic acid C2H2O4

usuc_278 = (12.*12.*uc_278 + 22.*1.*uh_278 + 11.*16.*uo_278) ./ (12.*12 + 22.*1 + 11.*16);
usuc_320 = (12.*12.*uc_320 + 22.*1.*uh_320 + 11.*16.*uo_320) ./ (12.*12 + 22.*1 + 11.*16);

uPropane123TriCarboxylicAcid_278 = (12.*12.*uc_278 + 8.*1*uh_278 + 6.*16.*uo_278) ./ (12.*12 + 8.*1 + 6.*16);
uPropane123TriCarboxylicAcid_320 = (12.*12.*uc_320 + 8.*1*uh_320 + 6.*16.*uo_320) ./ (12.*12 + 8.*1 + 6.*16);

uPinonicAcid_278 = (10.*12.*uc_278 + 16.*1*uh_278 + 3.*16.*uo_278) ./ (10.*12 + 16.*1 + 3.*16);
uPinonicAcid_320 = (10.*12.*uc_320 + 16.*1*uh_320 + 3.*16.*uo_320) ./ (10.*12 + 16.*1 + 3.*16);

uPinene_278 = (10.*12.*uc_278 + 16.*1*uh_278 + 0.*16.*uo_278) ./ (10.*12 + 16.*1 + 0.*16);
uPinene_320 = (10.*12.*uc_320 + 16.*1*uh_320 + 0.*16.*uo_320) ./ (10.*12 + 16.*1 + 0.*16);

usoot_278=uc_278;
usoot_320=uc_320;

%%_____________________________________________________________
%%___________________Calculate Cross inorganic Sections________

inorg278{1}=(2*14*un_278+8*1*uh_278+1*32*us_278+4*16*uo_278)...
    /(2*14+8*2+1*32+4*16); %% (NH4)2SO4
inorg320{1}=(2*14*un_320+8*1*uh_320+1*32*us_320+4*16*uo_320)...
    /(2*14+8*2+1*32+4*16); %% (NH4)2SO4

inorg278{2}=(2*14*un_278+4*1*uh_278+3*16*uo_278)/...
    (2*14+4*1+3*16);%% NH4NO3
inorg320{2}=(2*14*un_320+4*1*uh_320+3*16*uo_320)/...
    (2*14+4*1+3*16);%% NH4NO3

inorg278{3}= (1*23*una_278+1*14*un_278+3*16*uo_278)/...
    (1*23+1*14+3*16); %% NaNO3
inorg320{3}=(1*23*una_320+1*14*un_320+3*16*uo_320)/...
    (1*23+1*14+3*16); 

inorg278{4}= (1*39*uk_278+1*14*un_278+3*16*uo_278)/...
    (1*23+1*14+3*16); %% KNO3
inorg320{4}=(1*23*uk_320+1*14*un_320+3*16*uo_320)/...
    (1*23+1*14+3*16); 

inorg278{5}= (2*23*una_278+1*32*us_278+4*16*uo_278)/...
    (2*23+1*32+4*16); %% Na2SO4
inorg320{5}=(1*23*una_320+1*32*us_320+4*16*uo_320)/...
    (1*23+1*32+4*16);

inorg278{6}= (1*23*una_278+1*35*ucl_278)/(1*23+1*35); %% NaCl
inorg320{6}=(1*23*una_320+1*35*ucl_320)/(1*23+1*35); 

inorg278{7}= (1*39*uk_278+1*35*ucl_278)/(1*39+1*35); %% KCl
inorg320{7}=(1*39*uk_320+1*35*ucl_320)/(1*39+1*35); 

inorg278{8}= (2*56*ufe_278+3*16*uo_278)/(2*56+3*16); %% Fe203
inorg320{8}=(2*56*ufe_320+3*16*uo_320)/(2*56+3*16);

inorg278{9}=(1*40*uca_278+1*12*uc_278+3*16*uo_278)/(1*40+1*12+3*16); %% CaCO3
inorg320{9}=(1*40*uca_320+1*12*uc_320+3*16*uo_320)/(1*40+1*12+3*16);

inorg278{10}=(1*65*uzn_278+1*16*uo_278)/(1*65+1*16); %% ZnO
inorg320{10}=(1*65*uzn_320+1*16*uo_320)/(1*65+1*16); %% ZnO

inorg278{11}=(1*207*upb_278+2*14*un_278+6*16*uo_278)/(1*207+2*14+6*16); %% Pb(NO3)2
inorg320{11}=(1*207*upb_320+2*14*un_320+6*16*uo_320)/(1*207+2*14+6*16); %% Pb(NO3)2

inorg278{12}=(2*27*ual_278+2*28*usi_278+9*16*uo_278+4*1*uh_278)/(2*27+2*28+9*16+4*1); %% Al2Si2O9H4
inorg320{12}=(2*27*ual_320+2*28*usi_320+9*16*uo_320+4*1*uh_320)/(2*27+2*28+9*16+4*1); %%Al2Si2O9H4

if strcmpi(compound,'adipic')==1
    uorg278=uad_278;
    uorg320=uad_320;
elseif strcmpi(compound,'sucrose') == 1
    uorg278 = usuc_278;
    uorg320 = usuc_320;
elseif strcmpi(compound,'tricarboxylic acid') == 1
	uorg278 = uPropane123TriCarboxylicAcid_278;
	uorg320 = uPropane123TriCarboxylicAcid_320;
elseif strcmpi(compound,'pinonic acid') == 1
	uorg278 = uPinonicAcid_278;
	uorg320 = uPinonicAcid_320;
elseif strcmpi(compound,'pinene') == 1
	uorg278 = uPinene_278;
	uorg320 = uPinene_320;
elseif strcmpi(compound,'NaCl')==1
    uinorg278=inorg278{6};
    uinorg320=inorg320{6};
elseif strcmpi(compound,'(NH4)2SO4') == 1
    uinorg278 = inorg278{1};
    uinorg320 = inorg320{1};
elseif strcmpi(compound, 'CaCO3')
	uinorg278 = inorg278{9};
	uinorg320 = inorg320{9};
end


























end